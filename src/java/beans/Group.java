package beans;

import java.io.Serializable;

/**
 *
 * @author giorgio
 */
public class Group implements Serializable
{
    private Integer groupID;
    private Integer groupAdminID;
    private String groupName;
    private Boolean groupIsPrivate;
    private Boolean groupIsOpen;
    private String groupDataCreation;
    private String groupOldName;

    public Group()
    {
        
    }
    
    /**
     * @return the groupID
     */
    public Integer getGroupID()
    {
        return groupID;
    }

    /**
     * @param groupID the groupID to set
     */
    public void setGroupID(Integer groupID)
    {
        this.groupID = groupID;
    }

    /**
     * @return the groupAdminID
     */
    public Integer getGroupAdminID()
    {
        return groupAdminID;
    }

    /**
     * @param groupAdminID the groupAdminID to set
     */
    public void setGroupAdminID(Integer groupAdminID)
    {
        this.groupAdminID = groupAdminID;
    }

    /**
     * @return the groupName
     */
    public String getGroupName()
    {
        return groupName;
    }

    /**
     * @param groupName the groupName to set
     */
    public void setGroupName(String groupName)
    {
        this.groupName = groupName;
    }

    /**
     * @return the groupDataCreation
     */
    public String getGroupDataCreation()
    {
        return groupDataCreation;
    }

    /**
     * @param groupDataCreation the groupDataCreation to set
     */
    public void setGroupDataCreation(String groupDataCreation)
    {
        this.groupDataCreation = groupDataCreation;
    }

    /**
     * @return the groupIsPrivate
     */
    public Boolean getGroupIsPrivate()
    {
        return groupIsPrivate;
    }

    /**
     * @param groupIsPrivate the groupIsPrivate to set
     */
    public void setGroupIsPrivate(Boolean groupIsPrivate)
    {
        this.groupIsPrivate = groupIsPrivate;
    }

    /**
     * @return the groupIsOpen
     */
    public Boolean getGroupIsOpen()
    {
        return groupIsOpen;
    }

    /**
     * @param groupIsOpen the groupIsOpen to set
     */
    public void setGroupIsOpen(Boolean groupIsOpen)
    {
        this.groupIsOpen = groupIsOpen;
    }

    /**
     * @return the groupOldName
     */
    public String getGroupOldName()
    {
        return groupOldName;
    }

    /**
     * @param groupOldName the groupOldName to set
     */
    public void setGroupOldName(String groupOldName)
    {
        this.groupOldName = groupOldName;
    }
}
